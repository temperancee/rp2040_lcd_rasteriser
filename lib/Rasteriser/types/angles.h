// Input: Angle units - these are 2*degrees. So 0.5 degrees is 1, and 180 degrees is 360
// Output: sin(angle) in Q16.16 format
static inline q16 sin_q16(int32_t angle)
{
    // Double modulo to account for negative angle values
    int32_t index = (angle % 720 + 720) % 720;

    if (index <= 180) {
        return sin_lut[index];
    } else if (index <= 360) {
        return sin_lut[360 - index];
    } else if (index <= 540) {
        return -sin_lut[index - 360];
    } else {
        return -sin_lut[720 - index];
    }
}


// Input: Angle units - these are 2*degrees. So 0.5 degrees is 1, and 180 degrees is 360
// Output: cos(angle) in Q16.16 format
static inline q16 cos_q16(int32_t angle)
{
    return sin_q16(angle + 180);
}
